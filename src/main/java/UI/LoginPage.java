    private void applyFuncCos() {

        tm.transparentButton(LoginBtn);

        tm.transparentTextField(UsernameFld);
        tm.transparentPasswordField(PasswordFld);
        a.addLoginExitListeners(this, UsernameFld, PasswordFld);
        
        
        // Add placeholders
        tm.addPlaceholder(UsernameFld, "Username");
        tm.addPlaceholder(PasswordFld, "Password");

        // Attach toggle functionality with icons
        String showIconPath = getClass().getResource("/visible.png").getPath();
        String hideIconPath = getClass().getResource("/invisible.png").getPath();

        ImageIcon showIcon = ThemeManager.ResizeImage(showIconPath, 24, 24);
        ImageIcon hideIcon = ThemeManager.ResizeImage(hideIconPath, 24, 24);

        tm.addPasswordToggle(PasswordFld, ToggleBtn, showIcon, hideIcon);
        tm.toggle(ToggleBtn);

    }

